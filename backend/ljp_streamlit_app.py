def translate_single_chunk_indictrans(text, target_lang_code, tokenizer, model):
    """
    Translate a single chunk of text using IndicTrans v1
    """
    try:
        if not text or not text.strip():
            return text
        
        # Clean the input text
        text = text.strip()
        
        # Prepare input for IndicTrans - use the correct format
        # IndicTrans expects format: <2target_lang> input_text
        input_text = f"<2{target_lang_code}> {text}"
        
        # Tokenize the input text with proper settings
        inputs = tokenizer(
            input_text,
            return_tensors="pt",
            padding=True,
            truncation=True,
            max_length=256  # Reduced for better quality
        )
        
        # Generate translation with optimized parameters
        with torch.no_grad():
            generated_tokens = model.generate(
                **inputs,
                max_length=256,
                num_beams=4,  # Reduced beams for faster processing
                early_stopping=True,
                do_sample=False,
                pad_token_id=tokenizer.pad_token_id,
                eos_token_id=tokenizer.eos_token_id
            )
        
        # Decode the translation
        translated_text = tokenizer.batch_decode(
            generated_tokens,
            skip_special_tokens=True
        )[0]
        
        # Clean up the translation
        translated_text = translated_text.strip()
        
        # Remove the input prefix if it appears in output
        if translated_text.startswith(f"<2{target_lang_code}>"):
            translated_text = translated_text[len(f"<2{target_lang_code}>"):].strip()
        
        # Remove the original English text if it appears at the beginning
        if translated_text.startswith(text):
            translated_text = translated_text[len(text):].strip()
        
        return translated_text if translated_text else text
        
    except Exception as e:
        print(f"DEBUG: IndicTrans chunk translation error: {e}")
        return text  # Return original text if translation fails

