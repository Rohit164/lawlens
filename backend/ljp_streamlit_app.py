def chunk_text_for_translation(text):
    """
    STEP 3: SEMANTIC CHUNKING (MANDATORY)
    Split content into meaningful chunks of max 300-400 characters at logical boundaries
    CRITICAL: Do NOT split mid-sentence, translate chunk-by-chunk
    """
    try:
        chunks = []
        max_chunk_size = 350  # Reduced for better quality
        
        # Split by sentences first
        sentences = re.split(r'[.!?]+', text)
        
        current_chunk = ""
        for sentence in sentences:
            sentence = sentence.strip()
            if not sentence:
                continue
                
            # If adding this sentence would exceed limit, save current chunk
            if len(current_chunk + sentence) > max_chunk_size and current_chunk:
                chunks.append(current_chunk.strip())
                current_chunk = sentence
            else:
                current_chunk += (" " + sentence if current_chunk else sentence)
        
        # Add the last chunk
        if current_chunk.strip():
            chunks.append(current_chunk.strip())
        
        return chunks
        
    except Exception as e:
        print(f"DEBUG: Text chunking error: {e}")
        return [text]  # Return original text as single chunk

