def simplify_sentence_structure(text):
    """Break down complex sentences into simpler ones"""
    
    # Split long sentences
    sentences = text.split('.')
    simplified_sentences = []
    
    for sentence in sentences:
        sentence = sentence.strip()
        if not sentence:
            continue
            
        # Break down very long sentences
        if len(sentence) > 100:
            # Split on common conjunctions
            parts = re.split(r'\b(however|therefore|moreover|furthermore|nevertheless|consequently)\b', sentence, flags=re.IGNORECASE)
            for part in parts:
                part = part.strip()
                if part and not part.lower() in ['however', 'therefore', 'moreover', 'furthermore', 'nevertheless', 'consequently']:
                    simplified_sentences.append(part)
        else:
            simplified_sentences.append(sentence)
    
    # Join sentences back
    result = '. '.join(simplified_sentences)
    
    # Add simple explanations for remaining complex terms
    result = add_simple_explanations(result)
    
    return result

def add_simple_explanations(text):
    """Add simple explanations for any remaining complex terms"""
    
    # Additional simplifications
    simplifications = {
        'pursuant to': 'according to',
        'notwithstanding': 'despite',
        'whereas': 'while',
        'whereby': 'by which',
        'wherein': 'in which',
        'thereof': 'of it',
        'herein': 'in this document',
        'hereof': 'of this document',
        'hereunder': 'under this document',
        'aforementioned': 'mentioned above',
        'aforesaid': 'said before',
        'heretofore': 'before this',
        'henceforth': 'from now on',
        'forthwith': 'immediately',
        'ipso facto': 'by the fact itself',
        'mutatis mutandis': 'with necessary changes',
        'pro rata': 'proportionally',
        'ad hoc': 'for this specific purpose',
        'bonafide': 'genuine',
        'malafide': 'dishonest'
    }
    
    for complex_term, simple_term in simplifications.items():
        text = re.sub(r'\b' + re.escape(complex_term) + r'\b', simple_term, text, flags=re.IGNORECASE)
    
    return text

def predict_with_explanation(text, tokenizer, model):
    """Predict judgment with explainability"""
    try:
        # Make prediction
        inputs = tokenizer(text, truncation=True, padding=True, max_length=512, return_tensors="pt")
        
        with torch.no_grad():
            outputs = model(**inputs)
            predictions = torch.nn.functional.softmax(outputs.logits, dim=-1)
        
        prob_rejected = predictions[0][0].item()
        prob_accepted = predictions[0][1].item()
        
        prediction = "ACCEPTED" if prob_accepted > prob_rejected else "REJECTED"
        confidence = max(prob_accepted, prob_rejected)
        
        # Generate explanation using LIME
        def predict_proba(texts):
            results = []
            for text in texts:
                inputs = tokenizer(text, truncation=True, padding=True, max_length=512, return_tensors="pt")
                with torch.no_grad():
                    outputs = model(**inputs)
                    probs = torch.nn.functional.softmax(outputs.logits, dim=-1)
                results.append([probs[0][0].item(), probs[0][1].item()])
            return np.array(results)
        
        explainer = LimeTextExplainer(class_names=['REJECTED', 'ACCEPTED'])
        explanation = explainer.explain_instance(text, predict_proba, num_features=10, num_samples=100)
        
        important_words = []
        for word, importance in explanation.as_list():
            important_words.append({
                'word': word,
                'importance': importance,
                'impact': 'Positive' if importance > 0 else 'Negative'
            })
        
        return {
            'prediction': prediction,
            'confidence': confidence,
            'prob_accepted': prob_accepted,
            'prob_rejected': prob_rejected,
            'explanation': important_words,
            'success': True
        }
        
    except Exception as e:
        st.error(f"Prediction error: {e}")
        return {'success': False, 'error': str(e)}

