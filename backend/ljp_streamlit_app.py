def create_docx_report(case_text, simple_summary, language="English"):
    """Create a DOCX report with Unicode support for Indian languages"""
    try:
        doc = Document()
        
        # Title
        title = doc.add_heading('Legal Case Summary Report', 0)
        title.alignment = WD_ALIGN_PARAGRAPH.CENTER
        
        # Add language indicator
        lang_para = doc.add_paragraph(f'Language: {language}')
        lang_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        lang_para.runs[0].font.size = Pt(10)
        lang_para.runs[0].font.color.rgb = RGBColor(128, 128, 128)
        
        # Add date
        date_para = doc.add_paragraph(f'Generated: {datetime.now().strftime("%B %d, %Y")}')
        date_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        date_para.runs[0].font.size = Pt(10)
        date_para.runs[0].font.color.rgb = RGBColor(128, 128, 128)
        
        doc.add_paragraph()  # Spacer
        
        # Add summary content
        lines = simple_summary.split('\n')
        
        for line in lines:
            line = line.strip()
            if not line:
                doc.add_paragraph()  # Empty line
                continue
            
            # Remove markdown formatting
            clean_line = line.replace('**', '').replace('*', '')
            
            if clean_line.startswith('##'):
                # Heading
                heading_text = clean_line.replace('##', '').strip()
                # Remove emoji numbers if present
                heading_text = re.sub(r'[0-9]️⃣\s*', '', heading_text)
                doc.add_heading(heading_text, level=2)
            elif clean_line.startswith('•'):
                # Bullet point
                p = doc.add_paragraph(clean_line[1:].strip(), style='List Bullet')
                if p.runs:
                    p.runs[0].font.size = Pt(11)
            else:
                # Normal paragraph
                p = doc.add_paragraph(clean_line)
                if p.runs:
                    p.runs[0].font.size = Pt(11)
        
        # Footer
        doc.add_paragraph()
        footer = doc.add_paragraph('Generated by LawLens AI - Legal Document Simplification Platform')
        footer.alignment = WD_ALIGN_PARAGRAPH.CENTER
        footer.runs[0].font.size = Pt(9)
        footer.runs[0].font.color.rgb = RGBColor(128, 128, 128)
        
        # Save to buffer
        buffer = io.BytesIO()
        doc.save(buffer)
        buffer.seek(0)
        return buffer
        
    except Exception as e:
        st.error(f"Error creating DOCX: {str(e)}")
        return None

# NEW: Google Translate-based translation function
def translate_with_google(text, target_lang):
    """
    Translate text using Google Translate
    Fast translation with proper heading preservation and order maintenance
    """
    try:
        if target_lang == "English" or not text or not text.strip():
            return text
        
        # Get language code
        lang_code = GOOGLE_TRANSLATE_CODES.get(target_lang, "hi")
        
        # Split by emoji-numbered headings to maintain order
        # Pattern: ## followed by emoji number (1️⃣, 2️⃣, etc.)
        sections = re.split(r'(##\s*[0-9]️⃣[^\n]+)', text)
        
        translated_sections = []
        
        for i, section in enumerate(sections):
            if not section.strip():
                continue
            
            # Check if this is a heading with emoji number
            heading_match = re.match(r'(##\s*)([0-9]️⃣\s*)(.+)', section)
            
            if heading_match:
                # This is a heading - translate only the text part, keep ## and emoji
                markdown = heading_match.group(1)  # ##
                emoji = heading_match.group(2)      # 1️⃣
                heading_text = heading_match.group(3)  # The actual text
                
                try:
                    time.sleep(0.2)
                    result = translator.translate(heading_text, src='en', dest=lang_code)
                    # Reconstruct with markdown and emoji
                    translated_sections.append(f"{markdown}{emoji}{result.text}")
                except:
                    translated_sections.append(section)
            else:
                # Regular content - split into smaller chunks if needed
                if len(section) > 500:
                    # Split into sentences
                    sentences = re.split(r'([.!?।]+\s+)', section)
                    current_chunk = ""
                    
                    for j in range(0, len(sentences), 2):
                        sentence = sentences[j]
                        separator = sentences[j+1] if j+1 < len(sentences) else ""
                        full_sentence = sentence + separator
                        
                        if len(current_chunk) + len(full_sentence) > 500 and current_chunk:
                            # Translate current chunk
                            try:
                                time.sleep(0.3)
                                result = translator.translate(current_chunk, src='en', dest=lang_code)
                                translated_sections.append(result.text)
                            except:
                                translated_sections.append(current_chunk)
                            current_chunk = full_sentence
                        else:
                            current_chunk += full_sentence
                    
                    # Translate remaining chunk
                    if current_chunk.strip():
                        try:
                            time.sleep(0.3)
                            result = translator.translate(current_chunk, src='en', dest=lang_code)
                            translated_sections.append(result.text)
                        except:
                            translated_sections.append(current_chunk)
                else:
                    # Small section - translate as is
                    try:
                        time.sleep(0.3)
                        result = translator.translate(section, src='en', dest=lang_code)
                        translated_sections.append(result.text)
                    except Exception as e:
                        print(f"Translation error: {e}")
                        try:
                            time.sleep(1)
                            new_translator = Translator()
                            result = new_translator.translate(section, src='en', dest=lang_code)
                            translated_sections.append(result.text)
                        except:
                            translated_sections.append(section)
        
        # Join all sections in order
        final_translation = '\n\n'.join(translated_sections)
        
        # Clean up whitespace
        final_translation = re.sub(r' +', ' ', final_translation)
        final_translation = re.sub(r'\n\n\n+', '\n\n', final_translation)
        
        return final_translation.strip()
        
    except Exception as e:
        print(f"DEBUG: Major translation error: {e}")
        import traceback
        traceback.print_exc()
        return text  # Return original if translation fails

