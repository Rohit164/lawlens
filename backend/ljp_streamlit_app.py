        preserved_elements = {}
        element_counter = 0
        
        # Mark Act references (e.g., "Act XVII का 1937", "Act XI of 1938")
        act_pattern = r'\b(Act\s+[IVXLCDM]+\s+(?:का|of)\s+\d{4})\b'
        for match in re.finditer(act_pattern, text, re.IGNORECASE):
            placeholder = f"__ACT_{element_counter}__"
            preserved_elements[placeholder] = match.group(0)
            text = text.replace(match.group(0), placeholder, 1)
