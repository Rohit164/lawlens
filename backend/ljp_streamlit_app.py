def translate_chunk_with_indictrans(chunk, target_lang_code, tokenizer, model):
    """
    STEP 3: Translate each chunk using IndicTrans with AGGRESSIVE post-processing
    Ensures 100% translation - no English words remain
    """
    try:
        if not chunk.strip():
            return chunk
        
        # Use simple direct translation format for IndicTrans
        input_text = f"<2{target_lang_code}> {chunk}"
        
        # Tokenize
        inputs = tokenizer(
            input_text,
            return_tensors="pt",
            padding=True,
            truncation=True,
            max_length=512
        )
        
        # Generate translation with high quality settings
        with torch.no_grad():
            generated_tokens = model.generate(
                **inputs,
                max_length=512,
                num_beams=8,  # Higher beams for better quality
                early_stopping=True,
                do_sample=False,
                pad_token_id=tokenizer.pad_token_id,
                eos_token_id=tokenizer.eos_token_id,
                repetition_penalty=1.2,
                length_penalty=1.0
            )
        
        # Decode
        translated_text = tokenizer.batch_decode(
            generated_tokens,
            skip_special_tokens=True
        )[0]
        
        # Clean up
        translated_text = translated_text.strip()
        
        # Remove the input prefix if present
        if translated_text.startswith(f"<2{target_lang_code}>"):
            translated_text = translated_text[len(f"<2{target_lang_code}>"):].strip()
        
        # Remove original English if it appears at start
        if translated_text.startswith(chunk):
            translated_text = translated_text[len(chunk):].strip()
        
        # If translation failed or is empty, use fallback word-by-word translation
        if not translated_text or len(translated_text) < len(chunk) * 0.3:
            print(f"DEBUG: IndicTrans failed, using fallback translation")
            translated_text = fallback_word_translation(chunk, target_lang_code)
        
        return translated_text if translated_text else chunk
        
    except Exception as e:
        print(f"DEBUG: IndicTrans translation error: {e}")
        # Use fallback on error
        return fallback_word_translation(chunk, target_lang_code)

