    # Look for fact sections
    fact_keywords = ['facts', 'background', 'brief facts', 'case arose', 'dispute']
    sentences = text.split('.')
    
    background_sentences = []
    found_facts = False
    
    for sentence in sentences:
        sentence = sentence.strip()
        if any(keyword in sentence.lower() for keyword in fact_keywords):
            found_facts = True
            continue
        
        if found_facts and len(sentence) > 20:
            background_sentences.append(sentence)
            if len(background_sentences) >= 4:
                break
    
    if not background_sentences:
        # Use first few meaningful sentences
        for sentence in sentences[:10]:
            if len(sentence.strip()) > 30:
                background_sentences.append(sentence.strip())
                if len(background_sentences) >= 3:
                    break
    
    if background_sentences:
        return '. '.join(background_sentences) + '.'
    return "This case involves a legal dispute between parties that came before the court for resolution."

