def extract_text_from_file(uploaded_file):
    """Extract text from uploaded file with improved error handling"""
    try:
        file_type = uploaded_file.type
        file_name = uploaded_file.name
        
        if file_type == "application/pdf":
            try:
                pdf_reader = PyPDF2.PdfReader(io.BytesIO(uploaded_file.read()))
                text = ""
                for page_num, page in enumerate(pdf_reader.pages):
                    page_text = page.extract_text()
                    if page_text:
                        text += page_text + "\n"
                
                if not text.strip():
                    st.warning("⚠️ PDF appears to be empty or contains only images. Please try a text-based PDF or copy-paste the content.")
                    return None
                
                return text.strip()
                
            except Exception as e:
                st.error(f"❌ Error reading PDF: {str(e)}")
                st.info("💡 Try copying text from your PDF and pasting it in the manual input tab.")
                return None
            
        elif file_type == "application/vnd.openxmlformats-officedocument.wordprocessingml.document":
            try:
                doc = docx.Document(io.BytesIO(uploaded_file.read()))
                text = ""
                for paragraph in doc.paragraphs:
                    if paragraph.text.strip():
                        text += paragraph.text + "\n"
                
                if not text.strip():
                    st.warning("⚠️ Word document appears to be empty. Please check the file.")
                    return None
                
                return text.strip()
                
            except Exception as e:
                st.error(f"❌ Error reading Word document: {str(e)}")
                st.info("💡 Try copying text from your Word document and pasting it in the manual input tab.")
                return None
            
        elif file_type == "text/plain":
            try:
                text = str(uploaded_file.read(), "utf-8")
                if not text.strip():
                    st.warning("⚠️ Text file appears to be empty.")
                    return None
                return text.strip()
                
            except Exception as e:
                st.error(f"❌ Error reading text file: {str(e)}")
                return None
            
        else:
            st.error(f"❌ Unsupported file type: {file_type}")
            st.info("💡 Please upload PDF, DOCX, or TXT files only.")
            return None
            
    except Exception as e:
        st.error(f"❌ Unexpected error processing file: {str(e)}")
        st.info("💡 Please try uploading the file again or use manual input.")
        return None

