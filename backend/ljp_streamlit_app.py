def translate_section_block(section_text, source_lang, target_lang, tokenizer, model, retry=False):
    """Translate a complete section as one block"""
    try:
        if not section_text.strip():
            return section_text
        
        # Set the tokenizer source language
        tokenizer.src_lang = source_lang
        
        # Tokenize the entire section
        inputs = tokenizer(
            section_text,
            return_tensors="pt",
            padding=True,
            truncation=True,
            max_length=512
        )
        
        # Generate translation with different parameters for retry
        with torch.no_grad():
            if retry:
                # More aggressive translation for retry
                generated_tokens = model.generate(
                    **inputs,
                    forced_bos_token_id=tokenizer.lang_code_to_id[target_lang],
                    max_length=512,
                    num_beams=8,  # More beams for better quality
                    early_stopping=True,
                    do_sample=True,
                    temperature=0.7,
                    repetition_penalty=1.2
                )
            else:
                # Standard translation
                generated_tokens = model.generate(
                    **inputs,
                    forced_bos_token_id=tokenizer.lang_code_to_id[target_lang],
                    max_length=512,
                    num_beams=5,
                    early_stopping=True,
                    do_sample=False
                )
        
        # Decode the translation
        translated_text = tokenizer.batch_decode(
            generated_tokens,
            skip_special_tokens=True
        )[0]
        
        # Clean up the translation
        translated_text = translated_text.strip()
        
        print(f"DEBUG SECTION: '{section_text[:50]}...' -> '{translated_text[:50]}...'")
        
        return translated_text
        
    except Exception as e:
        print(f"DEBUG: Section translation error: {e}")
        return section_text

